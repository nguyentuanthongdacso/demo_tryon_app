  String translate(String key) {
    final code = locale.languageCode;
    if (_localizedStrings.containsKey(code) && _localizedStrings[code]!.containsKey(key)) {
      return _localizedStrings[code]![key]!;
    }
    // fallback to English
    return _localizedStrings['en']![key] ?? key;
  }

  // Convenience getters for common strings
  String get appTitle => translate('app_title');
}

class AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) {
    // Support Vietnamese and provide English fallback for others
    return true;
  }

  @override
  Future<AppLocalizations> load(Locale locale) async {
    // Use the selected locale if supported, otherwise fallback to English
    final supportedCodes = AppLocalizations._localizedStrings.keys;
    final code = locale.languageCode;
    final effective = supportedCodes.contains(code) ? locale : const Locale('en');
    return AppLocalizations(effective);
  }

  @override
  bool shouldReload(covariant LocalizationsDelegate<AppLocalizations> old) => false;
}
